
public class B2Reto {

    public static void main(String[] args) {

        // Definimos Las Variables
        int numero = 0;

        // Validamos Si El Numero Es Positivo, Negativo O Cero
        if (numero > 0) {

            System.out.println("Positivo");

        } else if (numero < 0) {

            System.out.println("Negativo");

        } else {

            System.out.println("Cero");

        }

    }

}
