
public class C5Reto {

    public static void main(String[] args) {

        // Definimos Variables
        int suma = 0;

        // Estructura Repetitiva Que Va A Sumar Los Numeros Entre El 1 Al 5
        for (int i = 1; i <= 5; i++) {

            // Suma Y Asignacion
            suma += i;

        }

        // Imprimimos 
        System.out.println("Suma = " + suma);

    }

}
