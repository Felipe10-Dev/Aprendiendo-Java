
public class C3Reto {

    public static void main(String[] args) {

        // Estructura Repetitiva Para Imprimir Los Pares Entre 1 Y 20 
        for (int i = 1; i <= 20; i++) {

            // Validamos Si El Numero Es Par
            if (i % 2 == 0) {

                System.out.println(i);

            }

        }

    }

}
