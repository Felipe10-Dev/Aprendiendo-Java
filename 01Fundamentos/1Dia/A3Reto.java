
public class A3Reto {

    public static void main(String[] args) {

        // Conversion De Numeros
        // Conversion Manual
        double precio = 29.99;
        int precioEntero = (int) precio;

        // Imprimimos
        System.out.println("Precio Entero = " + precioEntero);

        // Conversion Automatica
        int numeroEntero = 9;
        double enteroDecimal = numeroEntero;

        // Imprimimos
        System.out.println("Numero Decimal = " + enteroDecimal);

    }

}
