
public class A2Reto {

    public static void main(String[] args) {

        // Definimos Las Variables
        int a = 10;
        int b = 2;

        // Variable Que Almacenara Todos Los Resultados
        int resultado;

        // Suma
        resultado = a + b;
        System.out.println(a + " + " + b + " = " + resultado);

        // Resta
        resultado = a - b;
        System.out.println(a + " - " + b + " = " + resultado);

        // Multiplicacion
        resultado = a * b;
        System.out.println(a + " * " + b + " = " + resultado);

        // Division
        resultado = a / b;
        System.out.println(a + " / " + b + " = " + resultado);

        // Modulo O Residuo
        resultado = a % b;
        System.out.println(a + " % " + b + " = " + resultado);

    }

}
